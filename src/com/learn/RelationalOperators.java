package com.learn;

public class RelationalOperators {
	
	//RelationalOperators == , !=, >, <, >=, <=  

	public static void main(String[] args) {
		
      int a = 30;
      int b = 25;
      
      String x = "Bindhu";
      String y = "Lakshmi";
      
      boolean bb = a == b;
      System.out.println(bb);
      
      bb = x == y;
      System.out.println(bb);
            
      bb = a != b;
      System.out.println(bb);
      
      bb = x != y;
      System.out.println(bb);
      
      bb = a > b;
      System.out.println(bb);
      
      bb = a < b;
      System.out.println(bb);
      
      bb = a >= b;
      System.out.println(bb);
      
      bb = (a <= b);
      System.out.println(bb);
      
	}

}
