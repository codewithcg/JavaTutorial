package com.finalkey;

public final class Animal {

	String colour, name;
	int size;

	public void eat() {
		System.out.println("Animal: I am eating");
	}

	public void hunt() {
		System.out.println("Animal: I am hunting");
	}

	public void sleep() {
		System.out.println("Animal: I am sleeping");
	}

	public void run() {
		System.out.println("Animal: I am running");
	}
}
